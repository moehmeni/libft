int	ft_atoi(const char *str)
{
	int	n;
	int	neg;

	n = 0;
	neg = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
		if (*str++ == '-')
			neg = -1;
	while (*str >= '0' && *str <= '9')
	{
		n = (n * 10) + ((*str++) - 48);
	}
	return (n * neg);
}
